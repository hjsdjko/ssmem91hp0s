<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				shenqingbianhao : false,
				dongwumingcheng : false,
				dongwuzhonglei : false,
				fengmian : false,
				lingyangyuanyin : false,
				shenqingshijian : false,
				lingyangzhanghao : false,
				lingyangxingming : false,
				lianxidianhua : false,
				zhuzhi : false,
				zhanghao : false,
				xingming : false,
				sfsh : false,
				shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          shenqingbianhao: this.getUUID(),
          dongwumingcheng: '',
          dongwuzhonglei: '',
          fengmian: '',
          lingyangyuanyin: '',
          shenqingshijian: '',
          lingyangzhanghao: '',
          lingyangxingming: '',
          lianxidianhua: '',
          zhuzhi: '',
          zhanghao: '',
          xingming: '',
        },


        rules: {
          shenqingbianhao: [
          ],
          dongwumingcheng: [
          ],
          dongwuzhonglei: [
          ],
          fengmian: [
          ],
          lingyangyuanyin: [
          ],
          shenqingshijian: [
          ],
          lingyangzhanghao: [
          ],
          lingyangxingming: [
          ],
          lianxidianhua: [
          ],
          zhuzhi: [
          ],
          zhanghao: [
          ],
          xingming: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.shenqingshijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='shenqingbianhao'){
              this.ruleForm.shenqingbianhao = obj[o];
              this.ro.shenqingbianhao = true;
              continue;
            }
            if(o=='dongwumingcheng'){
              this.ruleForm.dongwumingcheng = obj[o];
              this.ro.dongwumingcheng = true;
              continue;
            }
            if(o=='dongwuzhonglei'){
              this.ruleForm.dongwuzhonglei = obj[o];
              this.ro.dongwuzhonglei = true;
              continue;
            }
            if(o=='fengmian'){
              this.ruleForm.fengmian = obj[o].split(",")[0];
              this.ro.fengmian = true;
              continue;
            }
            if(o=='lingyangyuanyin'){
              this.ruleForm.lingyangyuanyin = obj[o];
              this.ro.lingyangyuanyin = true;
              continue;
            }
            if(o=='shenqingshijian'){
              this.ruleForm.shenqingshijian = obj[o];
              this.ro.shenqingshijian = true;
              continue;
            }
            if(o=='lingyangzhanghao'){
              this.ruleForm.lingyangzhanghao = obj[o];
              this.ro.lingyangzhanghao = true;
              continue;
            }
            if(o=='lingyangxingming'){
              this.ruleForm.lingyangxingming = obj[o];
              this.ro.lingyangxingming = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
            if(o=='zhuzhi'){
              this.ruleForm.zhuzhi = obj[o];
              this.ro.zhuzhi = true;
              continue;
            }
            if(o=='zhanghao'){
              this.ruleForm.zhanghao = obj[o];
              this.ro.zhanghao = true;
              continue;
            }
            if(o=='xingming'){
              this.ruleForm.xingming = obj[o];
              this.ro.xingming = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.zhanghao!=''&&json.zhanghao) || json.zhanghao==0){
                this.ruleForm.lingyangzhanghao = json.zhanghao
            }
            if((json.xingming!=''&&json.xingming) || json.xingming==0){
                this.ruleForm.lingyangxingming = json.xingming
            }
            if((json.shouji!=''&&json.shouji) || json.shouji==0){
                this.ruleForm.lianxidianhua = json.shouji
            }
            if((json.zhuzhi!=''&&json.zhuzhi) || json.zhuzhi==0){
                this.ruleForm.zhuzhi = json.zhuzhi
            }
          }
        });

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`lingyangshenqing/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			if(this.ruleForm.shenqingbianhao){
				this.ruleForm.shenqingbianhao = String(this.ruleForm.shenqingbianhao)
			}
			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('lingyangshenqing/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`lingyangshenqing/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`lingyangshenqing/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      fengmianUploadChange(fileUrls) {
          this.ruleForm.fengmian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

